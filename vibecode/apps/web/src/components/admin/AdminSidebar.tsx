'use client';

import Link from 'next/link';
import { usePathname } from 'next/navigation';
import { useAdminAuth } from '@/contexts/admin/AdminAuthContext';

const navItems = [
  { href: '/admin-panel', label: 'Dashboard', icon: DashboardIcon },
  { href: '/admin-panel/users', label: 'Users', icon: UsersIcon },
  { href: '/admin-panel/moderation', label: 'User Reports', icon: ShieldIcon },
  { href: '/admin-panel/moderation/ai', label: 'AI Moderation', icon: BrainIcon },
  { href: '/admin-panel/challenges', label: 'Challenges', icon: TrophyIcon },
  { href: '/admin-panel/content', label: 'Content', icon: GridIcon },
  { href: '/admin-panel/analytics', label: 'Analytics', icon: ChartIcon },
];

export function AdminSidebar() {
  const pathname = usePathname();
  const { user, logout } = useAdminAuth();

  return (
    <aside className="w-64 bg-[#0f0f10] border-r border-neutral-800 flex flex-col h-screen sticky top-0">
      {/* Logo */}
      <div className="p-6 border-b border-neutral-800">
        <Link href="/admin-panel" className="flex items-center gap-2">
          <div className="w-8 h-8 rounded-lg bg-amber-500 flex items-center justify-center">
            <span className="text-white font-bold text-sm">OS</span>
          </div>
          <span className="font-semibold text-white">Admin Panel</span>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4 px-3 space-y-1 overflow-y-auto">
        {navItems.map((item) => {
          const isActive = pathname === item.href ||
            (item.href !== '/admin-panel' && pathname.startsWith(item.href));
          const Icon = item.icon;

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-amber-500 text-white'
                  : 'text-neutral-400 hover:bg-neutral-800 hover:text-white'
              }`}
            >
              <Icon className="w-5 h-5" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* User section */}
      <div className="p-4 border-t border-neutral-800">
        <div className="flex items-center gap-3 mb-3">
          {user?.avatarUrl ? (
            <img
              src={user.avatarUrl}
              alt={user.displayName}
              className="w-8 h-8 rounded-full"
            />
          ) : (
            <div className="w-8 h-8 rounded-full bg-neutral-800 flex items-center justify-center">
              <span className="text-xs font-medium">
                {user?.displayName?.[0]?.toUpperCase() || '?'}
              </span>
            </div>
          )}
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-white truncate">
              {user?.displayName || 'Admin'}
            </p>
            <p className="text-xs text-neutral-500 truncate">
              @{user?.username || 'admin'}
            </p>
          </div>
        </div>
        <button
          onClick={logout}
          className="w-full px-3 py-2 text-sm font-medium text-neutral-400 hover:text-red-500 hover:bg-neutral-800 rounded-lg transition-colors"
        >
          Sign Out
        </button>
      </div>
    </aside>
  );
}

